function paint(){
	with (mgraphics) {
        translate(width/tileCount/2, height/tileCount/2);
		set_source_rgba(1, 1, 1, 1);
		paint();
		set_source_rgba(0, 0, 0, circleAlpha);
        
        set_line_width(mouseX/60);
        var counter = 0;
		for (var gridY=0; gridY<tileCount; gridY++) {
			for (var gridX=0; gridX<tileCount; gridX++) {
                
                var posX = width/tileCount*gridX;
                var posY = height/tileCount*gridY;
                
                ellipse(posX+theXVals[counter]*map(mouseX, 0.0, width, 0.0, 1.0) - (mouseY/15)/2, posY+theYVals[counter]*map(mouseX, 0.0, height, 0.0, 1.0) - (mouseY/15)/2, mouseY/15, mouseY/15);
                counter++; 
                stroke();
            }
		}
    }
}

function makeLines(){
		for (var i=0; i<tileCount*tileCount; i++) {
                theXVals[i] = ((Math.random()*width)-(width/2))/10;
                theYVals[i] = ((Math.random()*height)-(height/2))/10;
        }
        mgraphics.redraw();
}

	
function randomInt(min,max){
    return Math.floor(Math.random()*(max-min)+min);
}	
	
function onidle(x,y,but,cmd,shift,capslock,option,ctrl){
	mouseX = x;
	mouseY = y;
	mgraphics.redraw();
}

function onclick(x,y,but,cmd,shift,capslock,option,ctrl){
    makeLines();
}

function map(value, istart, istop, ostart, ostop){
    return ostart + (ostop - ostart) * ((value - istart) / (istop - istart));
}